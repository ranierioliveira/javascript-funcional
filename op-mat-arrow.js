//Construa funções para somar, subtrair, dividir e multiplicar, mas cada uma delas só pode ocupar uma linha.

const soma = (a, b) => a + b; 
const subtrair = (x, y) => x - y;
const dividir = (c, d) => c / d;
const multiplicar = (e, f) => e * f;








console.log(soma(5, 2));
console.log(subtrair(10, 5));
console.log(dividir(80, 8));
console.log(multiplicar(5, 4));
